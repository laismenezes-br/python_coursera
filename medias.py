#Entrada de dados (notas dos alunos)
nota1 = input("Digite a primeira nota: ")
nota2 = input("Digite a segunda nota: ")
nota3 = input("Digite a terceira nota: ")
nota4 = input("Digite a quarta nota: ")

#Para a média aritmética faz-se a conversão dos dados de entrada
média = (float(nota1)+float(nota2)+float(nota3)+float(nota4))/4
print("A média aritmética é",média)
